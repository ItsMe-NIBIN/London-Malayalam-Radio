                                is paid off, repeat the process with the next smallest one and keep going until all your
                                debts are paid.</li>
                            <li>Avalanche Method: List your debts from the highest to the lowest interest rate. Make
                                minimum payments on all except the one with the highest interest rate. Pay this one off
                                in larger increments until the debt is paid off. Repeat the process with the next
                                highest interest rate and keep going until all your debts are paid off.</li>
